        public static List<string> GetRegionsForLinkType(GeoLinkTypes geoLinkType, List<string> existingRegions)
        {
            foreach (string existingRegion in existingRegions)
            {
                if (string.IsNullOrEmpty(existingRegion) || !RegionProximityUtil.SourceRegionToTargetRegionsRTTInMs.ContainsKey(existingRegion))
                {
                    throw new ArgumentException(string.Format(CultureInfo.InvariantCulture, "existingRegion {0} is not a valid region.", existingRegion));
                }
            }

            List<List<string>> regionsSatisfyingLinkTypePerRegion = new List<List<string>>();

            foreach (string existingRegion in existingRegions)
            {
                regionsSatisfyingLinkTypePerRegion.Add(RegionProximityUtil.GetRegionsForLinkType(geoLinkType, existingRegion));
            }

            if(regionsSatisfyingLinkTypePerRegion.Count < 1)
            {
                return new List<string>();
            }

            IEnumerable<string> regionsSatisfyingAllInputRegions = regionsSatisfyingLinkTypePerRegion[0].AsEnumerable();
            for (int i = 1; i < regionsSatisfyingLinkTypePerRegion.Count; i++)
            {
                regionsSatisfyingAllInputRegions = regionsSatisfyingAllInputRegions.Intersect(regionsSatisfyingLinkTypePerRegion[i]);
            }

            if (existingRegions.Except(regionsSatisfyingAllInputRegions).Any())
            {
                return new List<string>();
            }

            return regionsSatisfyingAllInputRegions.ToList();
        }

        public static List<string> GetRegionsForLinkType(GeoLinkTypes geoLinkType, string sourceRegion)
        {
            if (string.IsNullOrEmpty(sourceRegion) || !RegionProximityUtil.SourceRegionToTargetRegionsRTTInMs.ContainsKey(sourceRegion))
            {
                throw new ArgumentException("sourceRegion is not a valid region.");
            }

            List<string> regionsSatisfyingLinkType = new List<string>();

            long linkTypeThresholdInMs = RegionProximityUtil.GetLinkTypeThresholdInMs(geoLinkType);

            Dictionary<string, long> roundTripTimeFromSourceRegion = RegionProximityUtil.SourceRegionToTargetRegionsRTTInMs[sourceRegion];
            foreach (string region in roundTripTimeFromSourceRegion.Keys)
            {
                if (roundTripTimeFromSourceRegion[region] <= linkTypeThresholdInMs)
                {
                    regionsSatisfyingLinkType.Add(region);
                }
            }

            if (!regionsSatisfyingLinkType.Contains(sourceRegion))
            {
                return new List<string>();
            }

            return regionsSatisfyingLinkType;
        }

        public static List<string> GeneratePreferredRegionList(string sourceRegion)
        {
            if (string.IsNullOrEmpty(sourceRegion) || !RegionProximityUtil.SourceRegionToTargetRegionsRTTInMs.ContainsKey(sourceRegion))
            {
                throw new ArgumentException("sourceRegion is not a valid region.");
            }

            List<KeyValuePair<string, long>> regionRTTList = RegionProximityUtil.SourceRegionToTargetRegionsRTTInMs[sourceRegion].ToList();
            regionRTTList.Sort((x, y) => x.Value.CompareTo(y.Value));

            List<string> preferredLocationsList = new List<string>();
            foreach (KeyValuePair<string, long> regionRTT in regionRTTList)
            {
                preferredLocationsList.Add(regionRTT.Key);
            }

            return preferredLocationsList;
        }

